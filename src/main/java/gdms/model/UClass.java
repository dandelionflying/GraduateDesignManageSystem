package gdms.model;

public class UClass {
	private int cid;
	private String className;
	private int deptID;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return className;
	}
	public void setCname(String className) {
		this.className = className;
	}
	public int getDeptID() {
		return deptID;
	}
	public void setDeptID(int deptID) {
		this.deptID = deptID;
	}
	
}
