package gdms.model;

public class ST {
	private int stId;
	private int topicId;
	private String sId;

	public int getStId() {
		return stId;
	}

	public void setStId(int stID) {
		this.stId = stID;
	}

	public int getTopicId() {
		return topicId;
	}

	public void setTopicId(int topicID) {
		this.topicId = topicID;
	}

	public String getSid() {
		return sId;
	}

	public void setSid(String sid) {
		this.sId = sid;
	}

}
